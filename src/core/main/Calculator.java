package main;

public class Calculator {
	
	public int somme(int a, int b) {
		int result = a + b;
		return result;
	}
	
	public int multiplie(int a, int b) {
		int result = a * b;
		return result;
	}
	
	public int soustrait(int a, int b) {
		int result = a - b;
		return result;
	}
	
	public int divise(int a, int b) {
		int result = a / b;
		return result;
	}
}
